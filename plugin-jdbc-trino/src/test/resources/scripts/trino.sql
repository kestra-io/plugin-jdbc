DROP TABLE IF EXISTS trino_types;

CREATE TABLE trino_types (
    t_null VARCHAR(2),
    t_boolean BOOLEAN,
    t_tinyint TINYINT,
    t_smallint SMALLINT,
    t_integer INTEGER,
    t_bigint BIGINT,
    t_real REAL,
    t_double DOUBLE,
    t_decimal DECIMAL(10, 5),
    t_varchar VARCHAR(10),
    t_char CHAR(10),
    t_varbinary VARBINARY,
    t_json JSON,
    t_date DATE,
    t_time TIME(6),
    t_time_tz TIME WITH TIME ZONE,
    t_timestamp TIMESTAMP(6),
    t_timestamp_tz TIMESTAMP(6) WITH TIME ZONE,
    t_interval_year INTERVAL YEAR TO MONTH,
    t_interval_day INTERVAL DAY TO SECOND,
--     t_array ARRAY,
--     t_map MAP,
--     t_row ROW,
    t_ipaddress IPADDRESS,
    t_uuid UUID
);


